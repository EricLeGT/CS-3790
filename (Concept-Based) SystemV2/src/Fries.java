public class Fries {
    Size size;
    Sauce sauce;

    public Fries(Size size, Sauce sauce) {
        this.size = size;
        this.sauce = sauce;
    }

    public Size getSize() {
        return size;
    }

    public void setSize(Size size) {
        this.size = size;
    }

    public Sauce getSauce() {
        return sauce;
    }

    public void setSauce(Sauce sauce) {
        this.sauce = sauce;
    }

}
